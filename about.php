<div id="title">
<h1>Kaj je to Študentski svet FMF?</h1>
</div>

<p>Če se sprašujete, kaj je ŠS in kakšne so njegove naloge, ter na kakšen način
se zavzemamo za študente, vas vabimo, da prebrskate našo spletno stran. Upamo
in želimo, da bomo na ta način delo ŠS približali študentom, saj je ustanovljen
predvsem in ravno zaradi njih.</p>

<p>Prva vprašanja, ki se porajajo študentom ob omembi ŠS, so navadno naslednja:
&raquo;Kaj pa je to? Kaj pa tam delajo? A ima sploh kakšen namen? ... &laquo;
Začnimo lepo po vrsti.</p>

<p>Študentski svet je organ študentov v okviru fakultete. Je edini organ, ki
uradno predstavlja študente znotraj fakultete in na višjem nivoju še znotraj
Univerze. Kaj to pomeni? Pomeni, da je vloga ŠS v okviru fakultete velika. Smo
vez med študenti in fakulteto, imamo veliko pravic in seveda posledično tudi
nekaj dolžnosti:</p>

<ul>
  <li>Prva dolžnost je zastopanje študentov in njihovih pravic v vseh organih
    fakultete. Naš glas se sliši povsod, kjer je potrebno, t.j. na Senatu
    fakultete, na Znanstveno-pedagoških svetih, na Akademskem zboru.</li>
   <li>Ena pomembnejših pravic in dolžnosti je oblikovanje študentskega mnenja o
    napredovanju vseh pedagoških delavcev na fakulteti. Za te potrebe je ŠS FMF
    ustanovil posebno Komisijo, ki na podlagi pravilnika pripravi mnenja in jih
    posreduje ŠS v razpravo ter potrditev.</li>
  <li>Poskušamo reševati večje in manjše probleme, ki se pojavljajo tekom
    študijskega leta (npr. preobremenjenost študentov).</li>
  <li>Sodelujemo pri prenovi študijskih programov v okviru Bolonjske
    reforme.</li>
  <li>Podajamo mnenja o kandidatih za dekana fakultete.</li>
  <li>Pripravljamo mnenja o vseh aktih fakultete, ki se tičejo študentov in
    njihovih pravic (tudi dolžnosti), sodelujemo z RC, sodelujemo v ŠS Univerze,
    &hellip;</li>
</ul>

<p>Sedaj, ko vemo, zakaj ŠS obstaja in kaj dela, poglejmo še, kdo ga sestavlja.
ŠS sestavljamo študentje (v bistvu je to čisto logično, a ne?), in sicer 13
izvoljenih predstavnikov. Trenutne člane si lahko ogledate <a
href="/members">tukaj</a>.</p>

<p>Študentski svet se mesečno dobiva na rednih sejah, kjer obravnava zgoraj
našteto tematiko in trenutne pereče probleme. Poleg tega poznamo še dopisne in
izredne seje, prve so aktualne večinoma v času počitnic, druge pa, če bi tako
želel dekan ali pa študenti FMF.</p>

<p>Vedno smo na voljo za vprašanja in pobude na naslovu
<a href="mailto:studentski.svet@fmf.uni-lj.si">
  <strong>studentski.svet@fmf.uni-lj.si</strong>
</a>,
<a href="http://forum.fmf.si/c/studentski-organi/studentski-svet">
  <strong>podforumu študentskih strani</strong></a> ali v
<strong>sobi 206 na Oddelku za matematiko</strong>, vsa pomembna obvestila in
informacije, pa bomo poskušali objaviti tu na strani in na oglasnih deskah po
fakulteti.</p>

<p>Pa še za konec &ndash; sodelovanje v ŠS je častno (beri ni plačano).</p>

