natančen povzetek sej sveta in njihovo objavo ter posredno za dobro obveščenost
študentov.<br>
<strong>Skrbnik informacijske tehnologije</strong> skrbi za spletno stran in elektronski naslov Sveta ter ostalo
informacijsko podporo s pomočjo RC FMF.
