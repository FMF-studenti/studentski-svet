zadeve, ki se tičejo njegovega oddelka.<br>
<strong>Tajnik ŠS FMF</strong> zapisuje dogajanje med sejami in pripravi zapisnik sej Sveta. Skrbi za
natančen povzetek sej sveta in njihovo objavo ter posredno za dobro obveščenost
študentov.<br>
<strong>Predstavnik za stike z RC</strong> skrbi za spletno stran in elektronski naslov Sveta ter ostalo
informacijsko podporo s pomočjo RC FMF.
